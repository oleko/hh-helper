"""
Для отмеченной вакансии генерирует:
  1) resume_notes.md — что подчеркнуть/переставить/добавить в мастер-резюме под эту вакансию
     (коротко, тезисно);
  2) resume_full.md — полный текст резюме под эту вакансию, готовый к копированию/лёгкой правке;
  3) cover_letter.txt — готовое сопроводительное письмо (годится и в поле на hh.ru,
     и как email).

Модель работает от карьерной базы + текста вакансии; никогда не придумывает цифры,
которых нет в базе (явно прописано в промпте).
"""
from __future__ import annotations

import logging
import re

from .llm_provider import LLMProvider

log = logging.getLogger("tailor")

# YandexGPT на практике игнорирует просьбу разделить секции строкой "---" и вместо
# этого сам ставит markdown-заголовки вида "### Cover_letter" или **Resume_full**
# (жирным, без #) — эти паттерны ловят оба варианта как запасной способ найти
# границы секций, если буквальных "---" нет. \**  вокруг метки — для случая, когда
# модель обернула её в ** (жирным) вместо "#"-заголовка.
_SPLIT_RE = re.compile(r"\n\s*-{3,}\s*\n")
_RESUME_FULL_HEADING_RE = re.compile(
    r"(?im)^\s*#{0,4}\s*\**\s*(resume[ _-]?full|full[ _-]?resume|полное\s*резюме)\s*\**\s*:?\s*$"
)
_COVER_LETTER_HEADING_RE = re.compile(
    r"(?im)^\s*#{0,4}\s*\**\s*(cover[ _-]?letter|сопроводительн\w*\s*письмо)\s*\**\s*:?\s*$"
)
# модель иногда подписывает начало секции голым словом-меткой сразу после разделителя
_LEADING_LABEL_RE = re.compile(
    r"(?im)^\s*#{0,4}\s*\**\s*(resume[ _-]?notes|resume[ _-]?full|full[ _-]?resume|"
    r"полное\s*резюме|cover[ _-]?letter|сопроводительн\w*\s*письмо)\s*\**\s*:?\s*\n+"
)

SYSTEM_PROMPT = """Ты — карьерный консультант, помогаешь адаптировать резюме и написать
сопроводительное письмо под конкретную вакансию.

СТРОГОЕ ПРАВИЛО: используй только факты и цифры из карьерной базы кандидата.
Никогда не изобретай новые достижения, цифры или опыт. Если для вакансии не хватает
подходящего кейса — так и скажи в notes, не выдумывай.

ВАЖНО ПРО КЛЮЧЕВЫЕ НАВЫКИ: если в тексте вакансии есть строка "Ключевые навыки
(по данным HH): ..." — это точный список, по которому и рекрутёр, и ATS-система
(Huntflow, Поток, E-Staff и т.п.) матчат резюме с вакансией автоматически. Каждый
навык из этого списка, который реально подтверждён в карьерной базе кандидата,
должен быть дословно (тем же словом/термином, не синонимом) вписан и в resume_full
(в блок навыков и/или в опыт), и в resume_notes как ключевое слово. Если навык
из списка НЕ подтверждён базой — не пиши его, а отметь пробел в resume_notes.

Верни ответ в виде ТРЁХ секций, разделённых строкой ровно из трёх дефисов "---" —
это единственный разделитель, не заменяй его markdown-заголовками ("###" и т.п.):

СЕКЦИЯ 1 (resume_notes) — по-русски, маркированный список, коротко:
- заголовок резюме для этой вакансии (см. ЖЁСТКОЕ ПРАВИЛО ниже)
- какие 3-5 достижений из базы вынести в начало и почему именно они бьют в требования вакансии
- какие ключевые слова из текста вакансии стоит естественно вплести (для ATS и рекрутера)
- что из мастер-резюме для ЭТОЙ вакансии лучше убрать/свернуть
- если релевантного опыта под ключевое требование вакансии в базе нет — прямо укажи этот пробел

ЖЁСТКОЕ ПРАВИЛО ПРО ЗАГОЛОВОК: заголовок резюме (первая строка resume_full) —
это ТОЧНОЕ название вакансии из строки "Должность: ..." в тексте вакансии, без
перефразирования и без добавления своих слов. Это не пожелание, а требование:
рекрутёр и ATS ищут по точному совпадению тайтла. Если у кандидата два трека
(A/B) и заголовок вакансии узкий/непривычный — всё равно бери его дословно,
разницу в позиционировании показывай через summary и порядок достижений, не
через переписанный заголовок.

ЖЁСТКОЕ ПРАВИЛО ПРО ПОРЯДОК ДОСТИЖЕНИЙ: внутри каждого места работы в
resume_full достижения-буллеты идут строго по убыванию силы цифры/факта —
самое конкретное и измеримое (число, %, ₽, масштаб) всегда первым буллетом,
общие/описательные формулировки — в конце или отдельным предложением-описанием
роли до буллетов. Рекрутёр читает первые 5-7 секунд — цифра должна попасться
сразу, а не третьим пунктом.

СЕКЦИЯ 2 (resume_full) — ПОЛНЫЙ текст резюме под эту вакансию в markdown, готовый
к копированию с минимальной правкой (не просто заметки, а связный документ):
- заголовок — точное название вакансии (см. ЖЁСТКОЕ ПРАВИЛО выше)
- краткое summary (2-4 предложения)
- опыт работы: релевантные места из карьерной базы, достижения-буллеты по
  убыванию силы цифры (см. ЖЁСТКОЕ ПРАВИЛО выше)
- ключевые навыки/компетенции
- языки, образование (если есть в базе)
Только факты из карьерной базы — ничего не придумывай.

СЕКЦИЯ 3 (cover_letter) — готовое сопроводительное письмо на русском (если вакансия
на английском или международная компания — на английском), 120-180 слов, без канцелярита,
конкретное, с 1-2 цифрами из карьерной базы, без воды и без слова "уважаемые".
"""


def _strip_label(text: str) -> str:
    return _LEADING_LABEL_RE.sub("", text.strip(), count=1).strip()


def _split_sections(raw: str) -> tuple[str, str, str]:
    parts = _SPLIT_RE.split(raw.strip())
    if len(parts) == 3:
        notes, resume_full, letter = parts
        return _strip_label(notes), _strip_label(resume_full), _strip_label(letter)

    full_match = _RESUME_FULL_HEADING_RE.search(raw)
    letter_match = _COVER_LETTER_HEADING_RE.search(raw)
    if full_match and letter_match and full_match.start() < letter_match.start():
        notes = raw[: full_match.start()]
        resume_full = raw[full_match.end() : letter_match.start()]
        letter = raw[letter_match.end() :]
    elif letter_match:
        # хотя бы письмо нашли — resume_full отдельно выделить не получилось
        notes = raw[: letter_match.start()]
        resume_full = "(не удалось выделить полное резюме отдельно — см. resume_notes)"
        letter = raw[letter_match.end() :]
    else:
        # модель вообще не разделила — отдаём всё как notes, чтобы ничего не потерять
        notes = raw
        resume_full = "(не удалось выделить полное резюме отдельно — см. resume_notes)"
        letter = "(не удалось выделить письмо отдельно — см. resume_notes)"
    return _strip_label(notes), _strip_label(resume_full), _strip_label(letter)


def tailor_for_vacancy(provider: LLMProvider, career_base_md: str, vacancy_text: str) -> tuple[str, str, str]:
    """Возвращает (resume_notes, resume_full, cover_letter)."""
    user_content = (
        f"КАРЬЕРНАЯ БАЗА КАНДИДАТА:\n{career_base_md}\n\n"
        f"---\n\nТЕКСТ ВАКАНСИИ:\n{vacancy_text}\n\n"
        "Дай resume_notes, resume_full и cover_letter по формату из системного промпта, "
        "раздели их строкой из трёх дефисов ---."
    )
    raw = provider.complete(SYSTEM_PROMPT, user_content, max_tokens=4000, temperature=0.4)
    return _split_sections(raw)
